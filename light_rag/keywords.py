"""
关键词提取模块 - 基于 jieba 分词和词性标注
从 embedding 服务迁移到本地执行
"""

import jieba
import jieba.posseg as pseg

# 中文停用词表（常见无意义词）
STOPWORDS = {
    # 代词
    "我", "你", "他", "她", "它", "我们", "你们", "他们", "自己", "什么", "这个", "那个",
    "这些", "那些", "哪个", "哪里", "谁", "某", "某们", "各", "每",
    # 副词/连词
    "的", "了", "和", "是", "就", "都", "而", "及", "与", "或", "也", "又", "还", "在",
    "有", "被", "比", "等", "但", "如果", "因为", "所以", "虽然", "但是", "然后", "而且",
    # 量词/助词
    "个", "只", "些", "把", "次", "回", "下", "上", "中", "里", "到", "得", "地",
    # 常见无意义词
    "想", "要", "会", "能", "可以", "一下", "一点", "一种", "一个", "一些", "可能",
    "大概", "也许", "应该", "必须", "需要", "请", "帮", "帮忙", "帮忙看", "帮忙查",
    "怎么", "怎样", "如何", "为什么", "吗", "呢", "吧", "啊", "呀", "哦", "嗯",
    "了解", "知道", "看看", "查查", "找找", "说", "问", "告诉", "给", "让",
    # 补充停用词
    "请问", "查", "看", "找", "算", "做", "用", "来", "去", "过", "起", "来",
}

# 保留的词性（名词、动词、形容词、专名等）
# n=名词, nr=人名, ns=地名, nt=机构, nz=其他专名
# v=动词, vn=名动词, a=形容词
ALLOWED_POS = {"n", "nr", "ns", "nt", "nz", "v", "vn", "vg", "a", "an", "ad", "ng", "t"}

# 自定义词典（专业术语，防止被拆分）
CUSTOM_WORDS = [
    # 技术
    ("RAG", 100, "nz"),
    ("Claude", 100, "nz"),
    ("Embedding", 100, "nz"),
    ("Chroma", 100, "nz"),
    ("Hook", 100, "nz"),
    ("API", 100, "nz"),
    ("向量数据库", 100, "nz"),
    ("知识库", 100, "n"),
    # HR 相关
    ("年假", 100, "n"),
    ("请假", 100, "vn"),
    ("请假制度", 100, "n"),
    ("调休", 100, "n"),
    ("加班", 100, "vn"),
    ("社保", 100, "n"),
    ("公积金", 100, "n"),
]

# 添加自定义词到 jieba
for word, freq, pos in CUSTOM_WORDS:
    jieba.add_word(word, freq, pos)


def extract_keywords(text: str) -> str:
    """
    从文本中提取关键词，返回关键词拼接的字符串。

    流程：
    1. jieba 分词 + 词性标注
    2. 过滤停用词
    3. 保留名词、动词、形容词
    4. 拼接返回
    """
    words = pseg.cut(text)
    keywords = []

    for word, pos in words:
        word = word.strip()
        # 跳过空词和停用词
        if not word or word in STOPWORDS:
            continue
        # 跳过纯数字和单字符（除非是专业术语）
        if word.isdigit() or (len(word) == 1 and not word.isalpha()):
            continue
        # 保留指定词性
        if pos in ALLOWED_POS or len(word) >= 4:  # 4字以上默认保留（专有名词）
            keywords.append(word)

    # 如果提取不到关键词，返回原文本（兜底）
    if not keywords:
        return text

    return " ".join(keywords)
